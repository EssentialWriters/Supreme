function get_user_balance($conn, $phone_number) {
    $sql = "SELECT SUM(Amount + profit) as balance FROM transactions WHERE PhoneNumber = ?";
    $stmt = $conn->prepare($sql);
    $stmt->bind_param("s", $phone_number);
    $stmt->execute();
    $result = $stmt->get_result();

    if ($result->num_rows > 0) {
        $row = $result->fetch_assoc();
        return $row['balance'];
    } else {
        return 0;
    }
